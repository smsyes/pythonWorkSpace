def getControlShape(shape):
    """ 
    Get the position of a control shape that is part of the CONTROL_SHAPES
    dictionary.

    :param str shape:
    :raises ValueError: When the control shape cannot be found
    """
    shapePositions = CONTROL_SHAPES.get(shape)
    
    if not shapePositions:
        raise ValueError(
            "ControlShape {0} cannot be found!".format(shape)
        )

    return shapePositions
